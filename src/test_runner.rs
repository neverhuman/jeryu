struct TestRoutingInference {
    tags: Vec<String>,
    risk_class: String,
    timeout_secs: u64,
    rationale: Vec<String>,
}

fn infer_test_tags(command: &str) -> Vec<String> {
    infer_test_routing(command).tags
}

fn infer_test_routing(command: &str) -> TestRoutingInference {
    let lower = command.to_ascii_lowercase();

    let untrusted_patterns = [
        "security",
        "chaos",
        "ip_exfiltration",
        "ip guard",
        "ip-guard",
        "sandbox",
        "fuzz",
    ];
    if untrusted_patterns
        .iter()
        .any(|pattern| lower.contains(pattern))
    {
        return TestRoutingInference {
            tags: vec!["untrusted".to_string()],
            risk_class: "untrusted".to_string(),
            timeout_secs: 1800,
            rationale: vec!["matched untrusted/risky command pattern".to_string()],
        };
    }

    let build_patterns = [
        "deployctl",
        "payloadctl",
        "build-local",
        "publish-rc",
        "cargo check",
        "cargo build",
        "cargo nextest",
        "cargo test --workspace",
        "cargo test -p veox-deploy",
        "cargo test -p veox-bootstrap",
        "cargo test -p veox-rc-gate",
        "cargo test -p cargo-vrc",
        "cargo test -p cargo-aer",
        "cargo test -p cargo-witness",
        "cargo test -p nht",
        "test-local-built",
        "test-local-rc",
        "deploy-canary",
    ];
    if build_patterns.iter().any(|pattern| lower.contains(pattern)) {
        return TestRoutingInference {
            tags: vec!["build".to_string()],
            risk_class: "build".to_string(),
            timeout_secs: 1200,
            rationale: vec!["matched build-heavy command pattern".to_string()],
        };
    }

    TestRoutingInference {
        tags: vec!["default".to_string()],
        risk_class: "default".to_string(),
        timeout_secs: max(600, TestRunOpts::default().timeout_secs),
        rationale: vec!["no heavy/risky pattern matched; using default runner".to_string()],
    }
}

/// Create a file on a branch using the "create" action.
async fn create_file_on_branch(
    client: &GitlabClient,
    project_id: i64,
    branch: &str,
    file_path: &str,
    content: &str,
    message: &str,
) -> Result<()> {
    client
        .create_file(project_id, branch, file_path, content, message)
        .await
}

/// Wait for a pipeline to appear on a branch.
#[allow(dead_code)]
async fn wait_for_pipeline(
    client: &GitlabClient,
    project_id: i64,
    ref_name: &str,
    max_attempts: u32,
) -> Result<i64> {
    for _ in 0..max_attempts {
        let pipelines = client.list_pipelines(project_id, Some(ref_name)).await?;
        if let Some(p) = pipelines.first() {
            return Ok(p.id);
        }
        sleep(Duration::from_secs(2)).await;
    }
    anyhow::bail!("no pipeline appeared for ref '{}' after waiting", ref_name)
}

/// Wait for a test job within a pipeline to reach a terminal state.
async fn wait_for_test_result(
    client: &GitlabClient,
    project_id: i64,
    pipeline_id: i64,
    job_name: &str,
    timeout_secs: u64,
) -> Result<TestRunResult> {
    let deadline = tokio::time::Instant::now() + Duration::from_secs(timeout_secs);

    loop {
        if tokio::time::Instant::now() > deadline {
            return Ok(TestRunResult {
                pipeline_id,
                job_id: None,
                job_name: job_name.to_string(),
                status: "timeout".to_string(),
                duration_secs: Some(timeout_secs as f64),
                trace_tail: "Timed out waiting for job to complete".to_string(),
                passed: false,
            });
        }

        let jobs = client.list_pipeline_jobs(project_id, pipeline_id).await?;

        // Find our job (may have a suffix if retried)
        if let Some(job) = jobs.iter().find(|j| j.name == job_name) {
            match job.status.as_str() {
                "success" => {
                    let trace = client
                        .get_job_log_snippet(project_id, job.id, 2000)
                        .await
                        .unwrap_or_default();
                    return Ok(TestRunResult {
                        pipeline_id,
                        job_id: Some(job.id),
                        job_name: job_name.to_string(),
                        status: "success".to_string(),
                        duration_secs: job.queued_duration,
                        trace_tail: trace,
                        passed: true,
                    });
                }
                "failed" => {
                    let trace = client
                        .get_job_log_snippet(project_id, job.id, 4000)
                        .await
                        .unwrap_or_default();
                    return Ok(TestRunResult {
                        pipeline_id,
                        job_id: Some(job.id),
                        job_name: job_name.to_string(),
                        status: "failed".to_string(),
                        duration_secs: job.queued_duration,
                        trace_tail: trace,
                        passed: false,
                    });
                }
                "canceled" | "skipped" => {
                    return Ok(TestRunResult {
                        pipeline_id,
                        job_id: Some(job.id),
                        job_name: job_name.to_string(),
                        status: job.status.clone(),
                        duration_secs: job.queued_duration,
                        trace_tail: String::new(),
                        passed: false,
                    });
                }
                _ => {
                    // Still running/pending/created
                }
            }
        }

        sleep(Duration::from_secs(3)).await;
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn infers_build_routing_for_deploy_commands() {
        let plan = plan_test_run(&TestRunOpts {
            project_id: 2,
            test_command: "cargo test -p veox-deploy".to_string(),
            job_name: None,
            image: "rust:1.92.0".to_string(),
            tags: None,
            timeout_secs: 600,
            ..TestRunOpts::default()
        });

        assert_eq!(plan.tags, vec!["build"]);
        assert_eq!(plan.risk_class, "build");
        assert!(plan.timeout_secs >= 1200);
    }

    #[test]
    fn infers_untrusted_routing_for_security_commands() {
        let plan = plan_test_run(&TestRunOpts {
            project_id: 2,
            test_command: "cargo test -p dougx security-scan".to_string(),
            job_name: None,
            image: "rust:1.92.0".to_string(),
            tags: None,
            timeout_secs: 600,
            ..TestRunOpts::default()
        });

        assert_eq!(plan.tags, vec!["untrusted"]);
        assert_eq!(plan.risk_class, "untrusted");
        assert!(plan.timeout_secs >= 1800);
    }

    #[test]
    fn defaults_to_default_routing_for_simple_commands() {
        let plan = plan_test_run(&TestRunOpts {
            project_id: 2,
            test_command: "cargo test -p veox-testctl".to_string(),
            job_name: None,
            image: "rust:1.92.0".to_string(),
            tags: None,
            timeout_secs: 600,
            ..TestRunOpts::default()
        });

        assert_eq!(plan.tags, vec!["default"]);
        assert_eq!(plan.risk_class, "default");
    }

    #[test]
    fn ephemeral_ci_yaml_uses_isolated_clone_path() {
        let plan = plan_test_run(&TestRunOpts {
            project_id: 2,
            test_command: "cargo test -p jeryu".to_string(),
            job_name: Some("smoke".to_string()),
            image: "rust:1.92.0".to_string(),
            tags: Some(vec!["build".to_string()]),
            timeout_secs: 600,
            ..TestRunOpts::default()
        });

        let yaml = render_ephemeral_ci_yaml(&plan);

        assert!(yaml.contains("GIT_STRATEGY: clone"));
        assert!(yaml.contains(
            "GIT_CLONE_PATH: \"$CI_BUILDS_DIR/$CI_PROJECT_PATH_SLUG-jeryu-$CI_PIPELINE_ID-$CI_JOB_ID\""
        ));
        assert!(yaml.contains("    - cargo test -p jeryu"));
    }
}
